<h1>Долина огня</h1>
<p>	- парк штата Невада (Valley of Fire State Park, Nevada) расположен в пустыне в получасе езды на машине к западу 
	от Лас Вегаса. Летом здесь очень жарко, поэтому рекомендуется посещать парк в ранние утренние часы, с 05-06 до 
	09-10 часов утра, пока солнце не раскалило камни и его мягкие лучи раскрашивают окрестности в удивительно 
	яркие цвета.</p>

<p> В это время (до 09:00) будка рейнджера на въезде в парк еще закрыта, и необходимо самостоятельно оплатить его 
	посещение, запихнув в специально установленный ящик конвертик с 10 долларами (за один автомобиль). Корешок от 
	конверта нужно положить под лобовое стекло. Пропуск-абонемент в Национальные парки не действует, т.к. 
	парк принадлежит штату.</p>
	
<p>	Поскольку время посещения парка ограничено несколькими утреними часами, имеет смысл немного подготовиться и 
	предварительно решить, что именно вы собираетесь посмотреть. В парке много интересного, на 
	<a href="http://www.valley-of-fire.com/">официальном сайте</a> есть схема с указанием основных троп и 
	достопримечательностей. Кроме того есть энтузиасты и целые сообщества, изучающие все интересные образования и 
	публикующие их каталоги, с фотографиями и точными координатами.</p>

<p> Как показал мой опыт, даже имея координыты и подробное описание, что и где искать некоторые объекты найти 
	не просто. Из-за пересечного рельефа можно довольно долго блуждать вокруг какой0нибудь арки и не заметить ее. 
	При этом некоторые из них оказываются совершенно миниатюрными.</p>
	
