<h1>Каньон Брайс (Bryce Canyon)</h1>

<p>	Природа юго-запада США потрясает в первую очередь своим разнообразием. Несмотря на то, что в течении двухнедельной поездки 
    я практически каждый день делал одно и то же - гулял и рассматривал различные камни, 
    к такому разнообразию форм, цветов, .. да всего, совершенно не возможно привыкнуть. Каждый день дарит новые открытия.</p> 

<p> Достойное место в этом ряду самобытных природных явлений занимает не похожий ни на что из увиденного мной ранее каньон Брайс 
    с его оранжевыми склонами и необычными скальными образованиями - худусами. </p>

<p>	По легенде индейцев Навахо собранные в каньоне худусы - души умерших предков. Если долго гулять среди безмолвных фигур, можно 
    встретить своих. Узнать их конечно нельзя, но вы почувствуете. Кто и зачем собирает в каньоне армию мертвых индейцы не знают.</p>
    
